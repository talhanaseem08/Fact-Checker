import csv
import json
from pathlib import Path
from statistics import mean
from typing import List

from config import DATA_DIR, RESULTS_DIR
from .fact_checker import check_claim


def load_claims(path: Path) -> List[dict]:
    claims = []
    with path.open("r", encoding="utf-8") as handle:
        for line in handle:
            line = line.strip()
            if line:
                claims.append(json.loads(line))
    return claims


def _try_load_model():
    try:
        from .llm_utils import load_local_llm

        return load_local_llm(), None
    except Exception as exc:
        return None, str(exc)


def write_csv(path: Path, rows: List[dict]) -> None:
    fieldnames = [
        "claim",
        "expected_verdict",
        "predicted_verdict",
        "category",
        "confidence",
        "confidence_label",
        "source_count",
        "supporting_count",
        "contradicting_count",
        "neutral_count",
        "search_success",
        "warnings",
    ]
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def write_summary(path: Path, rows: List[dict], model_error: str | None) -> dict:
    total = len(rows)
    correct = sum(
        1 for row in rows if row["expected_verdict"] == row["predicted_verdict"]
    )
    avg_confidence = mean([float(row["confidence"]) for row in rows]) if rows else 0.0
    successful_searches = sum(1 for row in rows if row["search_success"])
    insufficient = sum(
        1 for row in rows if row["predicted_verdict"] == "Insufficient Evidence"
    )
    accuracy = (correct / total * 100) if total else 0.0

    summary = {
        "total": total,
        "correct": correct,
        "accuracy": accuracy,
        "avg_confidence": avg_confidence,
        "successful_searches": successful_searches,
        "insufficient": insufficient,
    }

    lines = [
        "# Evaluation Summary",
        "",
        "This file is generated by `python -m src.evaluation` using live web search. Results may change because web search results change over time.",
        "",
        f"- Total claims tested: {total}",
        f"- Verdict accuracy: {accuracy:.2f}% ({correct}/{total})",
        f"- Average confidence: {avg_confidence:.2f}",
        f"- Successful searches: {successful_searches}/{total}",
        f"- Insufficient evidence predictions: {insufficient}",
    ]
    if model_error:
        lines.extend(
            [
                "",
                "## Model Loading Note",
                "",
                "The local Qwen model could not be loaded during this run, so the pipeline used the deterministic evidence-based fallback answer.",
                "",
                f"Error: `{model_error}`",
            ]
        )
    lines.extend(
        [
            "",
            "## Per-Claim Results",
            "",
            "| # | Expected | Predicted | Confidence | Search | Claim |",
            "|---|---|---|---:|---|---|",
        ]
    )
    for index, row in enumerate(rows, start=1):
        claim = str(row["claim"]).replace("|", "\\|")
        search = "yes" if row["search_success"] else "no"
        lines.append(
            f"| {index} | {row['expected_verdict']} | {row['predicted_verdict']} | "
            f"{row['confidence']} | {search} | {claim} |"
        )
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return summary


def main() -> None:
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    claims = load_claims(DATA_DIR / "evaluation_claims.jsonl")
    model_bundle, model_error = _try_load_model()

    rows = []
    for item in claims:
        result = check_claim(
            item["claim"],
            model_bundle=model_bundle,
            generate_with_llm=model_bundle is not None,
        )
        rows.append(
            {
                "claim": item["claim"],
                "expected_verdict": item["expected_verdict"],
                "predicted_verdict": result["verdict"],
                "category": item.get("category", ""),
                "confidence": result["confidence"],
                "confidence_label": result.get("confidence_label", ""),
                "source_count": len(result.get("sources", [])),
                "supporting_count": len(result.get("supporting_evidence", [])),
                "contradicting_count": len(result.get("contradicting_evidence", [])),
                "neutral_count": len(result.get("neutral_evidence", [])),
                "search_success": bool(result.get("sources")),
                "warnings": " | ".join(result.get("warnings", [])),
            }
        )

    write_csv(RESULTS_DIR / "evaluation_results.csv", rows)
    summary = write_summary(RESULTS_DIR / "evaluation_summary.md", rows, model_error)
    print("Evaluation complete")
    print(f"Total claims tested: {summary['total']}")
    print(f"Verdict accuracy: {summary['accuracy']:.2f}% ({summary['correct']}/{summary['total']})")
    print(f"Average confidence: {summary['avg_confidence']:.2f}")
    print(f"Successful searches: {summary['successful_searches']}/{summary['total']}")
    print(f"Insufficient evidence predictions: {summary['insufficient']}")
    if model_error:
        print(f"Model loading note: {model_error}")


if __name__ == "__main__":
    main()

